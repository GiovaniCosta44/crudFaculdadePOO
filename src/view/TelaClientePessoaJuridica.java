        public void readJTable(){
        DefaultTableModel modelo = (DefaultTableModel) jTClientePessoaJuridica.getModel();
        modelo.setNumRows(0);
        ClientePessoaJuridicaDao cpjdao = new ClientePessoaJuridicaDao();
        
        
        cpjdao.readPj().stream().forEach((p) -> {    
            modelo.addRow(new Object[]{
                p.getId(),
                p.getCNPJ(),
                p.getIE(),
                p.getNome(),
                p.getEndereco(),
                p.getTelefone(),
                p.getEmail()
                
            });
            
        });
                
    }
        
        public void searchTableForCnpj(String cnpj){
        DefaultTableModel modelo = (DefaultTableModel) jTClientePessoaJuridica.getModel();
        modelo.setNumRows(0);
        ClientePessoaJuridicaDao cpjdao = new ClientePessoaJuridicaDao();
        
        
        cpjdao.searchPj(cnpj).stream().forEach((p) -> {    
            modelo.addRow(new Object[]{
                p.getId(),
                p.getCNPJ(),
                p.getIE(),
                p.getNome(),
                p.getEndereco(),
                p.getTelefone(),
                p.getEmail()
                
            });
            
        });
                
    }

